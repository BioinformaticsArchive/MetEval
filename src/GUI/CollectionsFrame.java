    private void addModelToRepoButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addModelToRepoButtonMouseClicked
        JFileChooser chooser = new JFileChooser();
       chooser.setMultiSelectionEnabled(true);
        File[] selected ;
        if(chooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION){
            selected = chooser.getSelectedFiles();
            for(File file:selected){
                if(!isXML(file)){
                    JOptionPane.showMessageDialog(this, "File "+file.getAbsolutePath() + " does not belong to accepted format \n(must be .xml or .sbml)");
                    continue;
                }
                if(!isRedundant(file.getName())){
                    app.addNewModelsToRepo(file);
                }
                else JOptionPane.showMessageDialog(this, "Model "+ file.getName()+" already exists in the repository");
            }
                this.updateRepoList();
        }
    }//GEN-LAST:event_addModelToRepoButtonMouseClicked
private boolean isXML(File file){
    if(file.isFile()){
        String name = file.getName();
        if(name.endsWith(".xml") || name.endsWith(".sbml") )return true;
        else return false;
    }else return false;
}
    private boolean isRedundant(String name){
    name = stripExtension(name);
    MetModel model = app.getRepo().getModelByName(name);
    if(model != null)return true;
    else return false;
}
private String stripExtension(String name){
    int pos = name.lastIndexOf(".");
    if(pos == -1)return name;
    name.substring(0, pos);
    return name;
}
