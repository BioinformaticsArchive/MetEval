    private void updateRepoList(){
        DefaultListModel repoListModel = new DefaultListModel();
        this.repoList.setModel(repoListModel);
        DefaultListModel colModel = (DefaultListModel) this.collectionList.getModel();
        if(repo != null)
        for(MetModel model:repo.getModels()){
            if(!colModel.contains(model))repoListModel.addElement(model);
        }
    }
    private void updateCollectionList(){
        DefaultListModel repoListModel = new DefaultListModel();
        this.collectionList.setModel(repoListModel);
        
        for(MetModel model:edited.getModels()){
            
            
            repoListModel.addElement(model);
        }
        updateRepoList();
    }
